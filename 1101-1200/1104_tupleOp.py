t1 = (15, 10, 15, 20, 25, 5, 15, 25, 30, 40)
t2 = ("apple","orange","banana","watermelon")

print("Tuple t1:", t1)
print("Length of t1:", len(t1))
print("Count of 25 in t1:", t1.count(25))
print("Index of 20 in t1:", t1.index(20))
print("Concatenation t1 + t2:", t1 + t2)
print("t1 repeated twice:", t1 * 2)
print("Is banana in t2:", "banana" in t2)
print("Minimum in t1:", min(t1))
print("Maximum in t1:", max(t1))
print("Sum of t1:", sum(t1))
print("Slice t1[1:4]:", t1[1:4])
print("Reverse t1:", t1[::-1])
print("Sorted t1:", sorted(t1))
print("Unique elements in t1:", tuple(set(t1)))
print("All elements > 10 in t1:", all(x > 10 for x in t1))
print("Any element == 25 in t1:", any(x == 25 for x in t1))
