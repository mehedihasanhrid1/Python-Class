# Integer
a = 10
print(a, type(a))

# Float
b = 3.14
print(b, type(b))

# String
c = "Hello, World!"
print(c, type(c))

# Boolean
d = True
print(d, type(d))

# List
e = [1, 2, 3, 4]
print(e, type(e))

# Tuple
f = (5, 6, 7)
print(f, type(f))

# Set
g = {8, 9, 10}
print(g, type(g))

# Dictionary
h = {'name': 'Alice', 'age': 25}
print(h, type(h))

# NoneType
i = None
print(i, type(i))

# Complex number
j = 2 + 3j
print(j, type(j))