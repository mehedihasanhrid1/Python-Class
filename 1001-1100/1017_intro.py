# Algoritm = Algorithm is a step-by-step process for solving a problem. It is a set of instructions that are followed in a specific order to achieve a desired outcome.

# Flowchat = Flowchart is a pictorial representation of an algorithm. It uses symbols and arrows to show the flow of control in a program or process.

# Syntax = Syntax is the set of rules that define the structure of a programming language. It includes the rules for writing statements, expressions, and other constructs in a programming language.

# Statement = Statement is a single line of code that performs a specific action. It can be a declaration, assignment, or any other operation that the programming language supports.

# Expression = Expression is a combination of variables, constants, and operators that evaluates to a value. It can be a mathematical expression, logical expression, or any other type of expression that the programming language supports.

# Comment = Comment is a piece of text in the code that is ignored by the compiler or interpreter. It is used to explain the code or provide additional information to the reader. Comments are not executed as part of the program.

